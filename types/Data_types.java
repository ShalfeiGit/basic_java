package types;

public class Data_types {
  public static void main(String[] args) {
    //Primitive data types
    int a = 10;
    byte b = 10;
    short c = 10;
    long d = 10L;
    float e = 10.0f;
    double f = 10.0;
    boolean j = true;
    char h = 'A';

    System.out.println(a + " " + b + " " + d  + " " + c + " " + d + " " + e + " " + f + " " + j + " " + h);

    //Reference data types
    String k = "Hello";
    System.out.println(k);

    //Array data types
    int[] l = {10, 20, 30};
    System.out.println(l);
  }
}
